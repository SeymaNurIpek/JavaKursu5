package Gun15;

public class A02_CarpimTablosu {
    public static void main(String[] args) {
        /*
        // 2 lerin çarpım tablosunu sormuştuk.
        // 2 x 1 =2
        // 2 x 2 =4
        // ....

        // 5 lere kadar halini yapınız

         */
        for (int j = 1; j <=5 ; j++) {
            for (int i = 1; i <=10; i++) {
                System.out.println(j+"x"+i+"="+ (i*j));

            }
            System.out.println();
        }





    }
}
