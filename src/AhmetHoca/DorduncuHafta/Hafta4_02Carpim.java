package AhmetHoca.DorduncuHafta;

public class Hafta4_02Carpim {
    public static void main(String[] args) {
        // Carpim Tablosunun 1, 2 ve 3 ler hanesinin tum
        // sonuclarini ekrana yazdiran bir program yaziniz.

        for (int i = 1; i <=3; i++) {
            for (int j = 1; j <=10; j++) {
                System.out.println(i+" * "+j+" = "+ (i*j));

            }
            System.out.println();

        }
    }
}
